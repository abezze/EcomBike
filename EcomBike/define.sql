
    create table anagrafica (
        tipo_indirizzo smallint not null check ((tipo_indirizzo between 0 and 5)),
        id bigint generated by default as identity,
        cap varchar(10) not null,
        codice_fiscale varchar(20),
        partita_iva varchar(20),
        telefono varchar(20) not null,
        utente_user_name varchar(20),
        nazione varchar(30) not null,
        citta varchar(50) not null,
        cognome varchar(50) not null,
        nome varchar(50) not null,
        via varchar(100) not null,
        primary key (id)
    );

    create table categoria (
        id bigint generated by default as identity,
        descrizione varchar(255),
        primary key (id)
    );

    create table dettaglio_ordine (
        product_code integer unique,
        quantita integer not null,
        id bigint generated by default as identity,
        ordine_id bigint,
        primary key (id)
    );

    create table indirizzo_spedizione (
        tipo_indirizzo smallint not null check ((tipo_indirizzo between 0 and 5)),
        id bigint generated by default as identity,
        cap varchar(10) not null,
        codice_fiscale varchar(20),
        partita_iva varchar(20),
        telefono varchar(20) not null,
        nazione varchar(30) not null,
        citta varchar(50) not null,
        cognome varchar(50) not null,
        nome varchar(50) not null,
        via varchar(100) not null,
        primary key (id)
    );

    create table messaggi_sistema (
        lang varchar(4) not null,
        code varchar(20) not null,
        messaggio varchar(255),
        primary key (lang, code)
    );

    create table ordine (
        data_ordine date,
        orario_ordine time(0),
        stato_ordine smallint check ((stato_ordine between 0 and 5)),
        id bigint generated by default as identity,
        ordine_indirizzo bigint unique,
        utente_id varchar(20),
        primary key (id)
    );

    create table pagamento (
        amount numeric(38,2),
        data_pagamento date,
        orario_pagamento time(0),
        id bigint generated by default as identity,
        ordine_pagamento bigint unique,
        tipo_pagamento bigint,
        primary key (id)
    );

    create table prodotto (
        peso numeric(38,2) not null,
        product_code integer not null,
        quantita integer not null,
        categoria_id bigint,
        produttore_id bigint,
        taglia varchar(20),
        colore varchar(200),
        descrizione varchar(200) not null,
        primary key (product_code)
    );

    create table produttore (
        id bigint generated by default as identity,
        codice_fiscale varchar(20),
        partita_iva varchar(20),
        marchio varchar(255),
        nome_azienda varchar(255),
        primary key (id)
    );

    create table tipo_pagamento (
        id bigint generated by default as identity,
        societa_creditrice varchar(255) not null,
        tipo_pagamento varchar(255) not null,
        primary key (id)
    );

    create table utente (
        role smallint check ((role between 0 and 3)),
        email varchar(20) not null,
        password varchar(20) not null,
        user_name varchar(20) not null,
        primary key (user_name)
    );

