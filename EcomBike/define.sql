
    create table anagrafica (
        id bigint generated by default as identity,
        cap varchar(10) not null,
        codice_fiscale varchar(20),
        partita_iva varchar(20),
        telefono varchar(20) not null,
        nazione varchar(30) not null,
        citta varchar(50) not null,
        cognome varchar(50) not null,
        nome varchar(50) not null,
        via varchar(100) not null,
        utente_user_name varchar(255) unique,
        primary key (id)
    );

    create table dettaglio_ordine (
        quantita integer not null,
        id bigint generated by default as identity,
        ordine_id bigint,
        prodotto varchar(255),
        primary key (id)
    );

    create table indirizzo_spedizione (
        id bigint generated by default as identity,
        cap varchar(10) not null,
        codice_fiscale varchar(20),
        partita_iva varchar(20),
        telefono varchar(20) not null,
        nazione varchar(30) not null,
        citta varchar(50) not null,
        cognome varchar(50) not null,
        nome varchar(50) not null,
        via varchar(100) not null,
        utente_user_name varchar(255) unique,
        primary key (id)
    );

    create table messaggi_sistema (
        lang varchar(4) not null,
        code varchar(20) not null,
        messaggio varchar(255),
        primary key (lang, code)
    );

    create table ordine (
        id bigint generated by default as identity,
        cliente varchar(255),
        primary key (id)
    );

    create table pagamento (
        amount numeric(38,2),
        data_pagamento date,
        orario_pagamento time(0),
        id bigint generated by default as identity,
        tipo_pagamento bigint,
        primary key (id)
    );

    create table tipo_pagamento (
        id bigint generated by default as identity,
        societa_creditrice varchar(255) not null,
        tipo_pagamento varchar(255) not null,
        primary key (id)
    );

    create table utente (
        role smallint check ((role between 0 and 3)),
        email varchar(255),
        password varchar(255),
        user_name varchar(255) not null,
        primary key (user_name)
    );

